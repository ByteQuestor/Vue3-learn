<template>
    <div class="person">
        <h2>姓名：{{ name }}</h2>
        <h2>年龄：{{ age }}</h2>
        <button @click="changeName">修改名字</button>
        <button @click="changeAge">修改年龄</button>
        <button @click="showTel">显示电话</button>
    </div>
</template>

<script>
export default{
    name:'Persion',
    data(){
        return{
            name:'张三',
            age:18,
            tel:'10000000000000'
        }
    },
    methods:{
        changeName(){
            this.name='王子阳'
        },
        changeAge(){
            this.age+=1
        },
        showTel(){
            alert(this.tel)
        }
    }
}
</script>

